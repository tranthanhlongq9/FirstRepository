// viết function tính diện tích hình vuông. S = a x a
//với a = 8 
function tinhSHinhVuong(a){
    return a * a;
}

console.log('Diện tích hình vuông: ',tinhSHinhVuong(8));

// viết function tính chu vi hình vuông. P = a x 4
//với a = 8
function chuViHinhVuong(a){
    return a * 4;
}

console.log('Chu vi hình vuông: ', chuViHinhVuong(8));

// viết function tính diện tích chữ nhật. S = a x b
// với a = 10 và b = 5
function dienTichHCN(a, b){
    return a * b;
}

console.log('Diện tích HCN: ',dienTichHCN(10,5));
